      def kanji_to_hiragana(name)
        # 既にひらがなやカタカナならそのまま返す
        if name =~ /\A[ァ-ンー]+\z/
          # カタカナの場合そのまま返す
          return name.tr("ァ-ン", "ぁ-ん")
        elsif name =~ /\A[ぁ-ん]+\z/
          # ひらがなもそのまま返す
          return name
        else
          # 漢字をひらがなに変換
          KANJI_TO_HIRAGANA[name] || name
        end
      end
